import sys
def factorial(num): 
    if num < 0: 
        print("Factorial de un número negativo no existe")

    elif num == 0: 
        return 1
        
    else: 
        fact = 1
        while(num > 1): 
            fact *= num 
            num -= 1
        return fact 

if len(sys.argv) == 1:
   while True:
       try:
          n=int(input("Debe ingresar un número: "))
          break
       except ValueError: 
           print("Debe ingresar un número valido")


print("Factorial ",n,"! es ", factorial(n)) 

